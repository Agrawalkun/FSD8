package Memory_Management.Conditional_operator_practice;

public class Question_2_34 {
    public static void main(String[] args) {
        int x = 76;
        int y = 'g';
        if (x < y || x == 76) {
            System.out.println(x);
        }
        if (y > x && x == y) {
            System.out.println(y);
        }
    }
}
