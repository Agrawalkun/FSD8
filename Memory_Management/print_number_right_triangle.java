public class print_number_right_triangle {
    public static void main(String[] args) {
        int n =3;
    for(int i =0; i<n; i++){
        for(int j=0;j<=i;j++){
            System.out.print(i+1 +" ");
        }
        System.out.println();
    }
    }
}
